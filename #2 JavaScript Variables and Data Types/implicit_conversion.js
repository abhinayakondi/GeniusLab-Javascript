// Implicit conversion ir coercion

//String to Number (Concatentation)
let res = 5 + "5";  
console.log(res); // "55"

//Boolean to Number
//Automatically converts true to 1 and false to 0
let res2 = true + 1;  
console.log(res2); // 2

//String to Number (Equality Comparison ==)
let res3 = ("5" == 5);  
console.log(res3); // true

